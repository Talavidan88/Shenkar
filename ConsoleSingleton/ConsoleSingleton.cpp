#include "ConsoleSingleton.h"
ConsoleEventHandler* ConsoleSingleton::_instance = 0;

ConsoleEventHandler* ConsoleSingleton::GetInstance() {
	if (!_instance)
	{
		_instance = new ConsoleEventHandler();
		return _instance;
	}
	return _instance;
}